ll memo[101][101];

ll dp(int order, int descents){
    if(descents < 0) return 0;
    ll &ans = memo[order][descents];
    if(ans != -1) return ans;
    if(order == 0 && descents == 0){
        return ans =1;
    }
    if(order == 0){
        return ans = 0;
    }
    ans = 0;
    ll ans1 = smod(dp(order-1, descents)*(descents + 1), MOD); // add the latest between descents -> descents doesn't change or at the end
    ll ans2 = smod(dp(order-1, descents-1)*(order-descents), MOD); // order - 2 inbetween, (descents-1) descents, +1 at begining
    ans = smod(ans1 + ans2, MOD);
    return ans;
}


