const int max_n=100001;
bitset<max_n> p;
int s;
int sieve(int counter){
    p.set();
    for(ll i =2;i<s; ++i){
        if(!p[i]) continue;
        counter--;
        if(counter==0) return i;
        for(ll j = i*i; j < s; j+=i){
            if(p[j]==1){
                p[j]=0;
                counter--;
                if(counter==0) return j;
            }
        }
    }
    return -1;
}


