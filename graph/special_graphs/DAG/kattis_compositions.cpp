ll dp[31];
void solve() 
{
    // Basic DP
    int cnt, n, m, k; cin >> cnt >> n >> m >> k;
    set<int> toMiss;
    while(m <= n){
        toMiss.insert(m);
        m+=k;
    }
    memset(dp, 0, sizeof dp);
    dp[0] = 1;
    for(int i=1; i<=n; ++i){
        for(int j=1; j<=i; ++j){
            if(toMiss.count(j)) continue;
            dp[i] += dp[i-j];
        }
    }
    cout <<cnt << " "<< dp[n];

}

